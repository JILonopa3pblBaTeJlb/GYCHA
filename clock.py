# clock.py — Генератор ASCII-часов для видеотрансляции
import time
from datetime import datetime, timedelta

def get_font_map() -> dict[str, list[str]]:
    """
    Определяет матрицу шрифта для цифр и спецсимволов.
    Шрифт состоит из 3 строк. Каждая цифра имеет фиксированную ширину 4 символа.
    Разделители (двоеточие, пробел) имеют ширину 3 символа.
    """
    # l1, l2, l3 — три уровня (строки) ASCII-шрифта
    l1 = " ▀█  ▀▀▄ ▀▀▄ █ █ █▀▀ ▄▀▀ ▀▀█ ▄▀▄ ▄▀▄ ▄▀▄"
    l2 = "  █  ▄▀   ▀▄  ▀█ ▀▀▄ █▀▄ ▄▀  ▄▀▄  ▀█ █/█"
    l3 = " ▀▀▀ ▀▀▀ ▀▀    ▀ ▀▀   ▀  ▀    ▀  ▀▀   ▀ "

    chars = {}
    # Нарезаем строки на блоки по 4 символа для цифр от 1 до 9 и 0
    # Порядок в исходных строках соответствует цифрам: 1, 2, 3, 4, 5, 6, 7, 8, 9, 0
    for i in range(1, 11):
        char_key = str(i % 10)
        start, end = (i - 1) * 4, i * 4
        chars[char_key] = [l1[start:end], l2[start:end], l3[start:end]]

    # Слэш для даты (ширина 4 символа)
    chars["/"] = [
        "   █",
        " ▄▀ ",
        " ▀  "
    ]

    # Двоеточие для времени (ширина 3 символа)
    chars[":"] = [
        "   ",
        " ▀ ",
        " ▀ "
    ]

    # Пробел-разделитель (ширина 3 символа)
    chars[" "] = [
        "   ",
        "   ",
        "   "
    ]
    
    return chars

def get_clock_lines(dt_obj: datetime) -> list[str]:
    """
    Преобразует объект datetime в список из трех строк ASCII-арта.
    Формат вывода: HH:MM DD/MM
    
    Параметры:
        dt_obj: Объект времени (уже скорректированный с учетом задержки стрима).
    """
    font = get_font_map()
    
    # Формируем текстовое представление (например, "20:45 14/04")
    time_str = dt_obj.strftime("%H:%M %d/%m")
    
    # Инициализируем три пустые строки для сборки результата
    output_lines = ["", "", ""]
    
    # Для каждого символа в строке времени находим его ASCII-глиф и добавляем к строкам
    for char in time_str:
        # Если символ не найден в шрифте, используем пустые заглушки (пробелы)
        glyph = font.get(char, ["   ", "   ", "   "])
        for i in range(3):
            output_lines[i] += glyph[i]
            
    return output_lines

if __name__ == "__main__":
    # Тестовый запуск: вывод текущего времени в консоль
    lines = get_clock_lines(datetime.now())
    for line in lines:
        print(line)
